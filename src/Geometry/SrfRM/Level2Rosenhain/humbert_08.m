function RosenhainHumbertPolynomial08Evaluated(ee)
    assert #ee eq 3;
    e1,e2,e3 := Explode(ee);
    return 
	16*e1^4*e2^2*e3^2 - 16*e1^4*e2^2*e3 - 16*e1^4*e2*e3^2 + 16*e1^4*e2*e3 - 
