	16*e1^3*e2^2*e3^2 + 16*e1^3*e2*e3^3 - 16*e1^3*e2*e3 - e1^2*e2^4 + 
	16*e1^2*e2^3*e3^3 - 16*e1^2*e2^3*e3 + 2*e1^2*e2^3 - 14*e1^2*e2^2*e3^2 + 
	14*e1^2*e2^2*e3 - e1^2*e2^2 - 16*e1^2*e2*e3^3 + 14*e1^2*e2*e3^2 + 
	2*e1^2*e2*e3 - e1^2*e3^4 + 2*e1^2*e3^3 - e1^2*e3^2 + 2*e1*e2^4*e3 - 
