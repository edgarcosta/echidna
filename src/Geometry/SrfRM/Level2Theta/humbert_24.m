function Level2ThetaNullHumbertPolynomials24(R)
    P<a00,a02,a20,a22> := PolynomialRing(R, 4 : Global);
    //print "Warning: returning only one component.";
    return {@
        256*a00^8*a20^2*a22^2 - 128*a00^7*a02^3*a20^2 - 128*a00^7*a02^3*a22^2 + 64*a00^6*a02^6 -
        128*a00^6*a02^2*a20^4 - 128*a00^6*a02^2*a22^4 + 64*a00^5*a02^5*a20^2 +
        64*a00^5*a02^5*a22^2 - 32*a00^5*a02*a20^6 - 352*a00^5*a02*a20^4*a22^2 -
        352*a00^5*a02*a20^2*a22^4 - 32*a00^5*a02*a22^6 + 240*a00^4*a02^4*a20^4 +
        480*a00^4*a02^4*a20^2*a22^2 + 240*a00^4*a02^4*a22^4 - 192*a00^4*a20^6*a22^2 +
        640*a00^4*a20^4*a22^4 - 192*a00^4*a20^2*a22^6 - 128*a00^3*a02^7*a20^2 -
        128*a00^3*a02^7*a22^2 + 288*a00^3*a02^3*a20^6 - 160*a00^3*a02^3*a20^4*a22^2 -
        160*a00^3*a02^3*a20^2*a22^4 + 288*a00^3*a02^3*a22^6 - 128*a00^2*a02^6*a20^4 -
        128*a00^2*a02^6*a22^4 + 124*a00^2*a02^2*a20^8 - 400*a00^2*a02^2*a20^6*a22^2 +
        1000*a00^2*a02^2*a20^4*a22^4 - 400*a00^2*a02^2*a20^2*a22^6 + 124*a00^2*a02^2*a22^8 -
        32*a00*a02^5*a20^6 - 352*a00*a02^5*a20^4*a22^2 - 352*a00*a02^5*a20^2*a22^4 -
        32*a00*a02^5*a22^6 + 20*a00*a02*a20^10 - 28*a00*a02*a20^8*a22^2 - 184*a00*a02*a20^6*a22^4 - 
        184*a00*a02*a20^4*a22^6 - 28*a00*a02*a20^2*a22^8 + 20*a00*a02*a22^10 +
        256*a02^8*a20^2*a22^2 - 192*a02^4*a20^6*a22^2 + 640*a02^4*a20^4*a22^4 -
        192*a02^4*a20^2*a22^6 + a20^12 + 6*a20^10*a22^2 + 15*a20^8*a22^4 + 20*a20^6*a22^6 +
        15*a20^4*a22^8 + 6*a20^2*a22^10 + a22^12
        @};
end function;
